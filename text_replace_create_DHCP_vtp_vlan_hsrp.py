def hsrp_active_config(SITE_CODE,DHCP_SCOPE_NAME,VLAN_ID):
        DHCP_SCOPE_ADDRESS = "10."+SITE_CODE+"."+str(VLAN_ID)
        DHCP_SERVER_ADDRESS = "10."+SITE_CODE+"0.1"
        print("interface Vlan"+str(VLAN_ID))
        print("description "+DHCP_SCOPE_NAME)
        print("ip address "+DHCP_SCOPE_ADDRESS+".1 255.255.255.0")
        print("ip helper-address "+DHCP_SERVER_ADDRESS)
        print("standby "+str(VLAN_ID)+" ip "+DHCP_SCOPE_ADDRESS+".3")
        print("standby "+str(VLAN_ID)+" priority 105")
        print("standby "+str(VLAN_ID)+" preempt delay minimum 60 reload")
        print()
