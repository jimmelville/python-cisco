def dhcp_config(SITE_CODE,SITE_NAME,DHCP_SCOPE_NAME,VLAN_ID):
        DHCP_SERVER_ADDRESS = "10."+SITE_CODE+"0.1"
        DHCP_SCOPE_ADDRESS = "10."+SITE_CODE+"."+str(VLAN_ID)
        DHCP_SCOPE_NETWORK = DHCP_SCOPE_ADDRESS+".0"
        print("netsh Dhcp Server "+DHCP_SERVER_ADDRESS+" add scope "+DHCP_SCOPE_NETWORK+" 255.255.255.0 "+DHCP_SCOPE_NAME+" "+DHCP_SCOPE_NAME)
        print("netsh Dhcp Server "+DHCP_SERVER_ADDRESS+" Scope "+DHCP_SCOPE_NETWORK+" set state 1")
        print("netsh Dhcp Server "+DHCP_SERVER_ADDRESS+" Scope "+DHCP_SCOPE_NETWORK+" add iprange "+DHCP_SCOPE_ADDRESS+".1 "+DHCP_SCOPE_ADDRESS+".254")
        print("netsh Dhcp Server "+DHCP_SERVER_ADDRESS+" Scope "+DHCP_SCOPE_NETWORK+" add excluderange "+DHCP_SCOPE_ADDRESS+".1 "+DHCP_SCOPE_ADDRESS+".3")
        print("netsh Dhcp Server "+DHCP_SERVER_ADDRESS+" Scope "+DHCP_SCOPE_NETWORK+" set optionvalue 51 DWORD \"604800\"")
        print("netsh Dhcp Server "+DHCP_SERVER_ADDRESS+" Scope "+DHCP_SCOPE_NETWORK+" set optionvalue 3 IPADDRESS "+DHCP_SCOPE_ADDRESS+".3")
        print("netsh Dhcp Server "+DHCP_SERVER_ADDRESS+" Scope "+DHCP_SCOPE_NETWORK+" set optionvalue 15 STRING \"domain.com\"")
        print("netsh Dhcp Server "+DHCP_SERVER_ADDRESS+" Scope "+DHCP_SCOPE_NETWORK+" set optionvalue 44 IPADDRESS \"10.0.0.2\" \"10.0.128.2\"")
        print("netsh Dhcp Server "+DHCP_SERVER_ADDRESS+" Scope "+DHCP_SCOPE_NETWORK+" set optionvalue 46 BYTE \"4\"")
        print("netsh Dhcp Server "+DHCP_SERVER_ADDRESS+" Scope "+DHCP_SCOPE_NETWORK+" set optionvalue 6 IPADDRESS 10.0.0.2 10.0.128.2")
        print()
